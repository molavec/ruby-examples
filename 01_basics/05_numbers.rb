# Es posible realizar operaciones aritmeticas con ruby

resultado = 2+2;
puts resultado;
puts resultado.class;

resultado = 9-3;
puts resultado;
puts resultado.class;

resultado = 9/4;
puts resultado;
puts resultado.class;

## notar el cambio del tipo en la variable
resultado = 9.0/4;
puts resultado;
puts resultado.class;


resultado = 2**3;
puts resultado;
puts resultado.class;

resultado = 5%2;
puts resultado;
puts resultado.class;